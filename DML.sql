-- INSERT : IT IS HEPS TO INSERT VALUES TO THE TABLE
CREATE TABLE CUSTOMERS_FEEDBACK(
ID INT PRIMARY KEY,
NAME VARCHAR(20),
MOBILE VARCHAR(10),
RATING int
);

