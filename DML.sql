-- UPDATE : IT WILL HELP TO UPDATE TABLE ( UPDATE 01 ID NUMBER CUSTOMER WANT TO CHANGE RATING 5)
UPDATE CUSTOMERS_FEEDBACK 
SET RATING = 5
WHERE ID = 01;

-- 2 OR MORE RECORDS UPDATE WHIN ONLY ONE ROW
UPDATE CUSTOMERS_FEEDBACK 
SET NAME= "SIMA",RATING=6
WHERE ID =04;

-- UPDATE MULTIPLE VALUES AND MULTIPLE FIELDS
UPDATE CUSTOMERS_FEEDBACK 
SET NAME=case 
     WHEN ID =04 THEN "SITA"
     WHEN ID =05 THEN "MANU"
     END,
     RATING = case
     WHEN ID =04 THEN 8
     WHEN ID =05 THEN 8
     END
WHERE ID IN (04,05);

-- DELETE : TO REMOVE RECORDS FROM A TABLE: it only row wise delete the records not delete column ( column delete is use to alter commond within drop)
DELETE FROM CUSTOMERS_FEEDBACK
WHERE ID = 05;

